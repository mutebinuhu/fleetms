    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $request->validate([
            'document_name'=>'required',
            'vehicle_id'=>'required',
            'attachment'=>'required',

        ]);
        $vehicledocument = new vehicledocument([
            'document_name' => str::ucfirst($request->get('document_name')),
            'vehicle_id' => $request->get('vehicle_id'),
            'description' => $request->get('description'),
            'attachment'=>$request->file('attachment')->store('vehicledocuments')
        ]);
        $vehicledocument->save();
        return back()->with('status','Document Added');
    }

